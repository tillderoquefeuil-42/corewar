#include "ft_printf.h"

void	lowercase(char **str)
{
	unsigned int i;

	i = 0;
	while ((*str)[i] != '\0')
	{
		if ((*str)[i] >= 'A' && (*str)[i] <= 'Z')
			(*str)[i] += 32;
		i++;
	}
}
